CREATE TABLE IF NOT EXISTS `elementos` (
  `id` CHAR(1) NOT NULL,
  `name` VARCHAR(255) NOT NULL,
  PRIMARY KEY (`id`)
);

CREATE TABLE IF NOT EXISTS `cuentas` (
  `id` CHAR(2) NOT NULL,
  `name` VARCHAR(255) NOT NULL,
  `id_elemento` CHAR(1) NOT NULL,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`id_elemento`) REFERENCES `elementos`(`id`) ON DELETE CASCADE
